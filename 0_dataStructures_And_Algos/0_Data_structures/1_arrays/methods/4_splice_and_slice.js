//! splice and  slice

const Array = [];

Array.splice(); // The splice() method can be used to add or delete items anywhere to an array: the first parameter determines  where an item will be added or deleted , the second determines how many items will be deleted , the third + determine what items will be added to the array :The splice() methods returns an array with the deleted items:

Array.slice(); //The slice() method slices out a piece of an array into a new array : The slice() method can take two arguments like slice(1, 3).or 1 = beginning and 3 = end
