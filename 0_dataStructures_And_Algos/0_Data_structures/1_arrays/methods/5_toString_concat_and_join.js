//! toString , concat and join

const Array = [];

Array.toString(); // the toString() method  converts an array to a string  of (comma separated) array values.

Array.concat(); // The concat() method creates a new array by merging (concatenating) existing arrays: it  can also take strings as arguments:

Array.join(); // the join() method creates and returns a new string by concatenating all of the elements in this array, separated by commas or a specified character separator string
